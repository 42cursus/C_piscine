#include "c04_test.h"

#define RED   "\033[0;31m"
#define GREEN "\033[0;32m"
#define CYAN  "\033[36m"
#define RESET "\e[0m"

void ft_print_title(char *title)
{
	printf("--------------------------------------------------\n");
	printf("> %s%s%s\n", CYAN, title, RESET);
	printf("--------------------------------------------------\n");
	fflush(stdout);
}